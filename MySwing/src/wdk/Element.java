package wdk;

public class Element {
	private String name;
	private boolean isOpenFile=false;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public boolean isOpenFile() {
		return isOpenFile;
	}

	public void setOpenFile(boolean isOpenFile) {
		this.isOpenFile = isOpenFile;
	}

	

}
